package Futebol;

public class torcida {
    private String nome;
    private int quantidade;
    private String cantos;
    private String mosaico;
    private String instrumentos;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }

    public String getCantos() {
        return cantos;
    }

    public void setCantos(String cantos) {
        this.cantos = cantos;
    }

    public String getMosaico() {
        return mosaico;
    }

    public void setMosaico(String mosaico) {
        this.mosaico = mosaico;
    }

    public String getInstrumentos() {
        return instrumentos;
    }

    public void setInstrumentos(String instrumentos) {
        this.instrumentos = instrumentos;
    }
}
