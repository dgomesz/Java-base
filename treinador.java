package Futebol;

public class treinador {
    private String nome;
    private int idade;
    private String estiloDeJogo;
    private float salario;
    private String treinamneto;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getEstiloDeJogo() {
        return estiloDeJogo;
    }

    public void setEstiloDeJogo(String estiloDeJogo) {
        this.estiloDeJogo = estiloDeJogo;
    }

    public float getSalario() {
        return salario;
    }

    public void setSalario(float salario) {
        this.salario = salario;
    }

    public String getTreinamneto() {
        return treinamneto;
    }

    public void setTreinamneto(String treinamneto) {
        this.treinamneto = treinamneto;
    }
}
