package Futebol;

public class jogador {
    private String nome;
    private int numeroDeCamisa;
    private int numeroDoVestiario;
    private float salario;
    private String time;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getNumeroDeCamisa() {
        return numeroDeCamisa;
    }

    public void setNumeroDeCamisa(int numeroDeCamisa) {
        this.numeroDeCamisa = numeroDeCamisa;
    }

    public int getNumeroDoVestiario() {
        return numeroDoVestiario;
    }

    public void setNumeroDoVestiario(int numeroDoVestiario) {
        this.numeroDoVestiario = numeroDoVestiario;
    }

    public float getSalario() {
        return salario;
    }

    public void setSalario(float salario) {
        this.salario = salario;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
