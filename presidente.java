package Futebol;

public class presidente {
    private String nome;
    private int idade;
    private String gerenciamento;
    private String lideranca;
    private int capital;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getGerenciamento() {
        return gerenciamento;
    }

    public void setGerenciamento(String gerenciamento) {
        this.gerenciamento = gerenciamento;
    }

    public String getLideranca() {
        return lideranca;
    }

    public void setLideranca(String lideranca) {
        this.lideranca = lideranca;
    }

    public int getCapital() {
        return capital;
    }

    public void setCapital(int capital) {
        this.capital = capital;
    }
}
